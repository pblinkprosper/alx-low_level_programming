#include "3-calc.h"
/**
 * op_add - this function returns the sum of  two integers
 * @a: parameter one
 * @b: parameter two
 *
 * Return: returns the sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - this function returns the subtraction of  two integers
 * @a: parameter one
 * @b: parameter two
 *
 * Return: returns the sum
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - this function returns the multiplication of  two integers
 * @a: parameter one
 * @b: parameter two
 *
 * Return: returns the sum
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - this function returns the division of  two integers
 * @a: parameter one
 * @b: parameter two
 *
 * Return: returns the sum
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - this function returns the remainder of the division of two integers
 * @a: parameter one
 * @b: parameter two
 *
 * Return: returns the sum
 */
int op_mod(int a, int b)
{
	return (a % b);
}
