#include "main.h"
/**
 * _islower - checks if a character is lowercase
 */
int _islower(int)
{
	int c1 = 'H';
	int c2 = 'o';
	int c3 = 108;

	if (_islower(c1))
	{
		_putchar(c1 + '0');
	}
	if (_islower(c2))
	{
		_putchar(c2 + '0');
	}
	if (_islower(c3))
	{
		_putchar(c3 + '0');
	}
	_putchar('\n');
}

